package br.com.pangare.gerador;

public class Jogo {

	private int numJogo;
	private String timeMandante;
	private String timeVisitante;
	private int rodada;
	/**
	 * @param timeMandante the timeMandante to set
	 */
	public void setTimeMandante(String timeMandante) {
		this.timeMandante = timeMandante;
	}
	/**
	 * @return the timeMandante
	 */
	public String getTimeMandante() {
		return timeMandante;
	}
	/**
	 * @param timeVisitante the timeVisitante to set
	 */
	public void setTimeVisitante(String timeVisitante) {
		this.timeVisitante = timeVisitante;
	}
	/**
	 * @return the timeVisitante
	 */
	public String getTimeVisitante() {
		return timeVisitante;
	}
	/**
	 * @param rodada the rodada to set
	 */
	public void setRodada(int rodada) {
		this.rodada = rodada;
	}
	/**
	 * @return the rodada
	 */
	public int getRodada() {
		return rodada;
	}
	/**
	 * @param numJogo the numJogo to set
	 */
	public void setNumJogo(int numJogo) {
		this.numJogo = numJogo;
	}
	/**
	 * @return the numJogo
	 */
	public int getNumJogo() {
		return numJogo;
	}
	

	
}
